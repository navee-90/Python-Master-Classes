# range 
# range is  a built -in-function
# It is used to generate a sequence of numbers,It is commonly used for loops
# syntax:
# range(stop)
# range(start, stop)
# range(start, stop, step)
# start: (Optional) The starting number. Default is 0.

# stop: (Required) The end of the range (not included).

# step: (Optional) The amount by which the numbers increase. Default is 1.

# print numbers from 0 to 9


for i in range(10):
    print(i)
#print Hello World! 5 times 
